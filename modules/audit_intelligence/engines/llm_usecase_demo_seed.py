def _slug(value: str) -> str:
    return "".join(ch.lower() if ch.isalnum() else "_" for ch in str(value or "")).strip("_")


def _build_pdf_bytes(text: str) -> bytes:
    payload = text.replace("(", "[").replace(")", "]").replace("\\", "/")
    stream = f"BT /F1 11 Tf 50 760 Td ({payload[:700]}) Tj ET"
    pdf = (
        "%PDF-1.4\n"
        "1 0 obj << /Type /Catalog /Pages 2 0 R >> endobj\n"
        "2 0 obj << /Type /Pages /Kids [3 0 R] /Count 1 >> endobj\n"
        "3 0 obj << /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] "
        "/Contents 4 0 R /Resources << /Font << /F1 5 0 R >> >> >> endobj\n"
        f"4 0 obj << /Length {len(stream)} >> stream\n{stream}\nendstream endobj\n"
        "5 0 obj << /Type /Font /Subtype /Type1 /BaseFont /Helvetica >> endobj\n"
        "xref\n0 6\n0000000000 65535 f \n0000000010 00000 n \n0000000064 00000 n \n"
        "0000000121 00000 n \n0000000247 00000 n \n0000000000 00000 n \n"
        "trailer << /Size 6 /Root 1 0 R >>\nstartxref\n330\n%%EOF\n"
    )
    return pdf.encode("utf-8")


def _build_docx_bytes(lines: list[str]) -> bytes:
    content = "".join(f"<w:p><w:r><w:t>{line}</w:t></w:r></w:p>" for line in lines)
    document_xml = (
        '<?xml version="1.0" encoding="UTF-8" standalone="yes"?>'
        '<w:document xmlns:w="http://schemas.openxmlformats.org/wordprocessingml/2006/main">'
        f"<w:body>{content}</w:body></w:document>"
    )
    types_xml = (
        '<?xml version="1.0" encoding="UTF-8"?>'
        '<Types xmlns="http://schemas.openxmlformats.org/package/2006/content-types">'
        '<Default Extension="rels" ContentType="application/vnd.openxmlformats-package.relationships+xml"/>'
        '<Default Extension="xml" ContentType="application/xml"/>'
        '<Override PartName="/word/document.xml" '
        'ContentType="application/vnd.openxmlformats-officedocument.wordprocessingml.document.main+xml"/>'
        "</Types>"
    )
    rels_xml = (
        '<?xml version="1.0" encoding="UTF-8"?>'
        '<Relationships xmlns="http://schemas.openxmlformats.org/package/2006/relationships">'
        '<Relationship Id="rId1" '
        'Type="http://schemas.openxmlformats.org/officeDocument/2006/relationships/officeDocument" '
        'Target="word/document.xml"/>'
        "</Relationships>"
    )
    out = BytesIO()
    with zipfile.ZipFile(out, "w", compression=zipfile.ZIP_DEFLATED) as zf:
        zf.writestr("[Content_Types].xml", types_xml)
        zf.writestr("_rels/.rels", rels_xml)
        zf.writestr("word/document.xml", document_xml)
    return out.getvalue()


def _build_file_bytes(spec: dict[str, Any]) -> bytes:
    ext = Path(spec["filename"]).suffix.lower()
    summary = (
        f"Application: {spec['application']}\n"
        f"Framework: {spec['framework']}\n"
        f"Control: {spec['control_id']}\n"
        f"Connector: {spec['source_connector']}\n"
        f"Evidence ID: {spec['evidence_id']}\n"
        f"Topic: {spec['topic']}\n"
    )
    if ext == ".json":
        body = {
            "evidence_id": spec["evidence_id"],
            "application": spec["application"],
            "framework": spec["framework"],
            "control_id": spec["control_id"],
            "connector": spec["source_connector"],
            "status": "Collected",
            "checks": ["evidence_integrity", "control_mapping", "custody_snapshot"],
            "notes": "Deterministic ECS demo evidence payload.",
        }
        if spec.get("canary"):
            body["canary"] = spec["canary"]
        import json
        return json.dumps(body, indent=2, sort_keys=True).encode("utf-8")
    if ext == ".pdf":
        return _build_pdf_bytes(summary + "Generated by ECS deterministic demo seeding.")
    if ext == ".docx":
        return _build_docx_bytes(
            [
                "ECS Audit Evidence Memo",
                f"Application: {spec['application']}",
                f"Framework: {spec['framework']}",
                f"Control: {spec['control_id']}",
                f"Connector Source: {spec['source_connector']}",
                f"Evidence ID: {spec['evidence_id']}",
            ],
        )
    return (summary + "Collection outcome: PASS\n").encode("utf-8")


def _extract_docx_text(content: bytes) -> str:
    """Extract plain text from DOCX main document part."""
    try:
        with zipfile.ZipFile(BytesIO(content), "r") as zf:
            xml_bytes = zf.read("word/document.xml")
    except Exception:
        return ""
    try:
        root = ET.fromstring(xml_bytes)
    except ET.ParseError:
        return ""
    ns = {"w": "http://schemas.openxmlformats.org/wordprocessingml/2006/main"}
    paragraphs: list[str] = []
    for para in root.findall(".//w:p", ns):
        runs = para.findall(".//w:t", ns)
        text = "".join((node.text or "") for node in runs)
        if text:
            paragraphs.append(text)
    return "\n".join(paragraphs)


def _sanitize_docx_text_for_sql(text: str) -> str:
    return text.replace("\x00", "")


def _build_demo_evidence_catalog() -> tuple[dict[str, Any], ...]:
    from modules.audit_intelligence.engines import technology_control_mapping as mapping

    applications = ("Net Banking", "Mobile Banking", "Payments", "Treasury", "Card Operations")
    connectors = ("sharepoint_graph", "jira", "servicenow_cmdb", "sonarqube", "confluence", "github")
    formats = (
        ("pdf", "application/pdf"),
        ("docx", "application/vnd.openxmlformats-officedocument.wordprocessingml.document"),
        ("json", "application/json"),
        ("txt", "text/plain"),
    )
    usable = [c for c in mapping.all_controls() if c.frameworks and c.control_id]
    usable.sort(key=lambda c: c.control_id)
    selected = usable[:_TARGET_EVIDENCE_COUNT]
    out: list[dict[str, Any]] = []
    for idx, control in enumerate(selected, start=1):
        ext, mime = formats[(idx - 1) % len(formats)]
        app = applications[(idx - 1) % len(applications)]
        connector = connectors[(idx - 1) % len(connectors)]
        frameworks = tuple(str(fw) for fw in control.frameworks if str(fw))
        fw = frameworks[0] if frameworks else ""
        topic = _slug(control.technology or "general")
        evidence_id = "EV-LLM-ENC-001" if idx == 1 else f"EV-DEMO-{idx:03d}"
        filename = (
            "netbanking_encryption_at_rest.json"
            if idx == 1
            else f"{_slug(app)}_{control.control_id.lower()}_{idx:02d}.{ext}"
        )
        out.append(
            {
                "evidence_id": evidence_id,
                "filename": filename,
                "application": app,
                "framework": fw,
                "frameworks": frameworks,
                "control_id": control.control_id,
                "mime_type": mime,
                "topic": topic,
                "source_connector": connector,
                "canary": _CANARY if idx == 1 else "",
            },
        )
    return tuple(out)


