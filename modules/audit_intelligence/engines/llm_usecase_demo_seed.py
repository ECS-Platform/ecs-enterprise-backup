def _ensure_object_store(root: Optional[Path] = None):
    from ecs_platform.storage import LocalObjectStore, get_object_store, reset_object_store, set_object_store

    # Prefer the existing configured object store (MinIO/S3) for real custody.
    # Only force a local store when a caller explicitly asks via object_store_root.
    if root is None:
        reset_object_store()
        return get_object_store()
    store = LocalObjectStore(root)
    set_object_store(store)
    return store


def _ensure_sql_persistence():
    from modules.audit_intelligence.services import persistence as pers
    from modules.audit_intelligence.services.sql_persistence import SqlAuditPersistence

    current = pers.get_persistence()
    if isinstance(current, SqlAuditPersistence):
        return current
    sql = SqlAuditPersistence()
    pers.set_persistence(sql)
    return sql


def _persist_postgres_evidence(spec: dict[str, Any], *, content: str, artifact: Any, source_url: str) -> None:
    """Persist seeded evidence into the existing PostgreSQL evidence repository."""
    from ecs_platform.repository.repository import EvidenceRepository

    metadata = dict(getattr(artifact, "metadata", {}) or {})
    metadata.update(
        {
            "evidence_id": str(getattr(artifact, "evidence_id", "") or spec.get("evidence_id", "")),
            "filename": str(getattr(artifact, "filename", "") or spec.get("filename", "")),
            "custody_mode": str(getattr(artifact, "custody_mode", "") or ""),
            "object_uri": str(getattr(artifact, "object_uri", "") or ""),
            "mime_type": str(getattr(artifact, "mime_type", "") or spec.get("mime_type", "")),
            "source_url": source_url,
            "source_connector": str(spec.get("source_connector") or "llm_usecase_demo"),
            "content_hash": str(getattr(artifact, "content_hash", "") or ""),
            "version": int(getattr(artifact, "version", 1) or 1),
        },
    )
    item = {
        "evidence_uid": str(getattr(artifact, "evidence_id", "") or spec.get("evidence_id", "")),
        "source_system": str(spec.get("source_connector") or "llm_usecase_demo"),
        "source_object_id": str(spec.get("evidence_id", "")),
        "object_type": str(spec.get("mime_type", "") or "text/plain"),
        "title": str(getattr(artifact, "filename", "") or spec.get("filename", "") or spec.get("evidence_id", "")),
        "content": content,
        "owner": "llm_usecase_demo_seed",
        "url": str(getattr(artifact, "object_uri", "") or source_url),
        "application": str(spec.get("application", "")),
        "metadata": metadata,
        "control_mapping": [str(spec.get("control_id", ""))] if spec.get("control_id") else [],
        "framework_mapping": [str(fw) for fw in (spec.get("frameworks") or ()) if str(fw)]
        or ([str(spec.get("framework", ""))] if spec.get("framework") else []),
    }
    repo = EvidenceRepository()
    repo.upsert_evidence(item)


def _ingest_one(
    spec: dict[str, Any],
    *,
    provider: Any = None,
    vector_store: Any = None,
) -> dict[str, Any]:
    from ecs_platform.evidence_indexing import index_evidence_version
    from modules.audit_intelligence.engines import evidence_repository as repo
    from modules.audit_intelligence.services import evidence_custody as custody

    path = _DEMO_DIR / spec["filename"]
    body = path.read_bytes()
    ext = Path(spec["filename"]).suffix.lower()
    if ext == ".docx":
        text = _sanitize_docx_text_for_sql(_extract_docx_text(body))
    else:
        text = body.decode("utf-8", errors="ignore")
    key = repo.make_evidence_key(spec["application"], spec["control_id"])
    version = 1
    existing = repo.get_latest(key)
    if existing and existing.evidence_id == spec["evidence_id"] and existing.custody_mode == "SNAPSHOT":
        source_url = f"file://{_DEMO_DIR.as_posix()}/{spec['filename']}"
        _persist_postgres_evidence(spec, content=text, artifact=existing, source_url=source_url)
        if provider is not None or vector_store is not None:
            idx = index_evidence_version(
                existing,
                normalized_text=text,
                snapshot_bytes=body,
                force=True,
                provider=provider,
                store=vector_store,
            )
        else:
            idx = {"ok": True, "skipped": True, "reason": "already_seeded"}
        return {
            "evidence_id": spec["evidence_id"],
            "filename": spec["filename"],
            "skipped": True,
            "version": existing.version,
            "object_uri": existing.object_uri,
            "indexing": idx,
        }

    if existing:
        version = int(existing.version) + 1

    source_connector = str(spec.get("source_connector") or "llm_usecase_demo")
    source_url = f"file://{_DEMO_DIR.as_posix()}/{spec['filename']}"
    cust = custody.resolve_custody(
        source_connector=source_connector,
        source_item_id=spec["evidence_id"],
        source_url=source_url,
        source_modified_at="2026-06-12T00:00:00Z",
        filename=spec["filename"],
        mime_type=spec["mime_type"],
        evidence_key=key,
        version=version,
        content=body,
        custody_mode=custody.CUSTODY_SNAPSHOT,
    )
    art = repo.store_evidence(
        control_id=spec["control_id"],
        content=text,
        asset_id=spec["application"],
        frameworks=(spec["framework"],),
        verdict="Collected",
        control_status="Evidence Available",
        evidence_quality=0.9,
        source="llm_usecase_demo",
        filename=spec["filename"],
        evidence_key=key,
        evidence_id=spec["evidence_id"],
        environment="Production",
        source_connector=source_connector,
        source_item_id=spec["evidence_id"],
        source_url=source_url,
        mime_type=spec["mime_type"],
        metadata={"topic": spec.get("topic", ""), "demo": "llm_usecase"},
        custody_mode=cust.custody_mode,
        source_modified_at="2026-06-12T00:00:00Z",
        object_uri=cust.object_uri,
        content_hash_override=cust.content_hash,
        size_bytes_override=cust.size_bytes,
    )
    _persist_postgres_evidence(spec, content=text, artifact=art, source_url=source_url)
    if provider is not None or vector_store is not None:
        idx = index_evidence_version(
            art,
            normalized_text=text,
            snapshot_bytes=body,
            force=True,
            provider=provider,
            store=vector_store,
        )
    else:
        idx = {"ok": True, "embedded_chunks": 0, "skipped": True, "reason": "default_hook"}
    return {
        "evidence_id": art.evidence_id,
        "filename": art.filename,
        "version": art.version,
        "object_uri": art.object_uri,
        "custody_mode": art.custody_mode,
        "content_hash": art.content_hash,
        "indexing": idx,
        "skipped": False,
    }


def _ensure_demo_files() -> None:
    _DEMO_DIR.mkdir(parents=True, exist_ok=True)
    for spec in DEMO_EVIDENCE:
        path = _DEMO_DIR / str(spec["filename"])
        if path.is_file():
            continue
        path.write_bytes(_build_file_bytes(spec))


def seed_llm_usecase_demo(
    *,
    force: bool = False,
    provider: Any = None,
    vector_store: Any = None,
    object_store_root: Optional[Path] = None,
    enable_sql: bool = True,
) -> dict[str, Any]:
    """Seed observations + physical evidence through the real ECS custody/index path."""
    global _SEEDED, _LAST_REPORT
    with _LOCK:
        if _SEEDED and not force:
            return dict(_LAST_REPORT)

        report: dict[str, Any] = {
            "ok": False,
            "observations": 0,
            "evidence_files": 0,
            "stored": [],
            "errors": [],
            "canary": _CANARY,
            "demo_dir": str(_DEMO_DIR),
        }
        prev_snap = os.environ.get("ECS_EVIDENCE_SNAPSHOT_ENABLED")
        prev_mode = os.environ.get("ECS_EVIDENCE_CUSTODY_MODE")
        prev_mime = os.environ.get("ECS_EVIDENCE_ALLOWED_MIME_TYPES")
        prev_wf = os.environ.get("AUDIT_WORKFLOW_ENABLED")
        try:
            os.environ["ECS_EVIDENCE_SNAPSHOT_ENABLED"] = "true"
            os.environ["ECS_EVIDENCE_CUSTODY_MODE"] = "SNAPSHOT"
            os.environ["ECS_EVIDENCE_ALLOWED_MIME_TYPES"] = (
                "application/pdf,"
                "application/json,"
                "application/vnd.openxmlformats-officedocument.wordprocessingml.document,"
                "text/plain,text/csv"
            )
            if enable_sql:
                os.environ["AUDIT_WORKFLOW_ENABLED"] = "true"
                _ensure_sql_persistence()
            _ensure_object_store(object_store_root)
            report["observations"] = _seed_observations()

            _ensure_demo_files()

            for spec in DEMO_EVIDENCE:
                try:
                    stored = _ingest_one(spec, provider=provider, vector_store=vector_store)
                    report["stored"].append(stored)
                except Exception as exc:  # noqa: BLE001
                    report["errors"].append(f"{spec['evidence_id']}:{type(exc).__name__}:{exc}")
            report["evidence_files"] = len(report["stored"])
            report["ok"] = not report["errors"]
            _SEEDED = True
        except Exception as exc:  # noqa: BLE001
            report["errors"].append(f"seed_failed:{type(exc).__name__}:{exc}")
        finally:
            if prev_snap is None:
                os.environ.pop("ECS_EVIDENCE_SNAPSHOT_ENABLED", None)
            else:
                os.environ["ECS_EVIDENCE_SNAPSHOT_ENABLED"] = prev_snap
            if prev_mode is None:
                os.environ.pop("ECS_EVIDENCE_CUSTODY_MODE", None)
